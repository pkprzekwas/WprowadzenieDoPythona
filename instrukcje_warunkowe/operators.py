"""Operatory"""


value = 840


if value > 5:
    print('The value is greater than 5.')


if value >= 1729:
    print('The value is less or equal than 1729.')


if value > 1 or value < 2000:
    print('The value is between 1 and 2000.')


if value % 2 == 1:
    print('The value is an odd number.')


if value != 1:
    print('The value is different than one.')


if value in [1, 2, 1729]:
    print('The value is in predicted list.')


if value not in [1, 2, 3]:
    print('The value is not in the [1, 2, 3] list.')
